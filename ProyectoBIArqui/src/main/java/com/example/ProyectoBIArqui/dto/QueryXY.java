package com.example.ProyectoBIArqui.dto;

public class QueryXY {
    String x;
    Integer y;

    public QueryXY(String x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
